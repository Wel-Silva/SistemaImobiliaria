public class Transacoes {
    private String dataTrasicao;
    private  int nroContrato;

    public String getDataTrasicao() {
        return dataTrasicao;
    }

    public void setDataTrasicao(String dataTrasicao) {
        this.dataTrasicao = dataTrasicao;
    }

    public int getNroContrato() {
        return nroContrato;
    }

    public void setNroContrato(int nroContrato) {
        this.nroContrato = nroContrato;
    }
}
